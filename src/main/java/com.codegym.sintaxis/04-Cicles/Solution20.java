public class Solution20 {
    public static void main(String[] args) throws Exception {
        Scanner teclado = new Scanner();
        int a, b, c = teclado.nextInt();
        if(b == c){
            System.out.println(1);
        }
        else if(a == c){
            System.out.println(2);
        }
        else if(b == a){
            System.out.println(3);
        }
    }
}
